    public GUIMain() {
        this.setTitle("CalculatorPro");
        calculator= new Calculator();
        initComponents();
        screenJTF.setEditable(false);
        keyboard1.setCalculator(calculator);
        keyboard1.setScreen(screenJTF);
        keyboard1.setHistory(historyPanel1);
        this.setLocationRelativeTo(null);
        
        swingPaint2.getDrawArea().addMouseListener(new MouseAdapter() { 
            public void mouseReleased(MouseEvent e) {
                
                try {
                    String key=swingPaint2.predict();
                    if (!key.contains("l")){
                        calculator.addElement(key);
                        screenJTF.setText(calculator.inScreen());
                    }else{
                        String s=calculator.calculate();
                        if(!s.contains("=")) return;
                        historyPanel1.addToHistory(s);
                        screenJTF.setText(s);
                    }
                }catch(Exception x){
                    System.err.println(x);
                }finally{
                    swingPaint2.clear();
                }
                
            }
        });
        
    }

